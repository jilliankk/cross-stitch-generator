/**
 * Find the closest DMC color to a given RGB value using Euclidean distance
 * in weighted RGB space (human perception weighting).
 */
export function findClosestDMC(r: number, g: number, b: number): DMCColor {
  let minDist = Infinity;
  let closest = DMC_COLORS[0];

  for (const color of DMC_COLORS) {
    // Weighted Euclidean distance — human eyes are more sensitive to green
    const dr = r - color.r;
    const dg = g - color.g;
    const db = b - color.b;
    const dist = 2 * dr * dr + 4 * dg * dg + 3 * db * db;

    if (dist < minDist) {
      minDist = dist;
      closest = color;
    }
  }

  return closest;
}

/**
 * Find the N closest DMC colors to a given RGB value.
 */
export function findClosestDMCColors(r: number, g: number, b: number, n: number): DMCColor[] {
  const distances = DMC_COLORS.map(color => {
    const dr = r - color.r;
    const dg = g - color.g;
    const db = b - color.b;
    const dist = 2 * dr * dr + 4 * dg * dg + 3 * db * db;
    return { color, dist };
  });

  distances.sort((a, b) => a.dist - b.dist);
  return distances.slice(0, n).map(d => d.color);
}
